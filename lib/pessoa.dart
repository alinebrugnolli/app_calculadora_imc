
class Pessoa {
  String? nome;  
  double? peso;
  double? altura;

  
  
  void setNome(String nome) {
    nome = nome;
  }

  String? getNome(String nome) {
    return nome;
  }  

  void setPeso(double peso) {
    peso = peso;
  }

  double? getPeso(double peso) {
    return peso;
  }

  void setAltura(double altura) {
    altura = altura;
  }

  double? getAltura(double altura) {
    return altura;
  }  
}